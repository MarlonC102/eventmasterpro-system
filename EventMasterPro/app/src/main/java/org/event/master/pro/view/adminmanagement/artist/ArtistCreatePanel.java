    private void saveArtistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveArtistActionPerformed
        String aDocumentType, aDocumentNumber, aName, aMail, aMusicalGenre, aPhoneNumber, aRequirements;
        double aPrice;
        aDocumentType = String.valueOf(documentTypeArtist.getSelectedItem());
        aDocumentNumber = artistDocumentNumber.getText();
        aName = artistName.getText();
        aMail = artistEmail.getText();
        aMusicalGenre = artistMusicalGenre.getText();
        aPhoneNumber = artistPhoneNumber.getText();
        aPrice = Double.parseDouble(artistPrice.getText());
        aRequirements = artistRequirements.getText();
        boolean message = adao.registerArtist(aDocumentType, aDocumentNumber, aName, aMail, aMusicalGenre, aPhoneNumber, aRequirements, aPrice);
        if (!message) {
            showAdminHome(container);

        }else{
            JOptionPane.showMessageDialog(null, "There is already an artist with this same document number");
        }
    }//GEN-LAST:event_saveArtistActionPerformed

